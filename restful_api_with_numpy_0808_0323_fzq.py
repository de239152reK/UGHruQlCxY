import numpy as np
from flask import Flask, request, jsonify
from flask_restful import Api, Resource

# Create Flask app instance
app = Flask(__name__)
api = Api(app)

# Define a resource for handling GET requests to '/data'
class DataResource(Resource):
    def get(self):
        """
        Handle GET requests for /data
        Returns a JSON object with random data generated by NumPy
        """
        try:
            # Generate a random array using NumPy
            data = np.random.rand(10).tolist()
            return jsonify(data)
        except Exception as e:
            # Return error message in case of an exception
            return jsonify({'error': str(e)}), 500

# Add the resource to the API
api.add_resource(DataResource, '/data')

# Define a resource for handling POST requests to '/predict'
class PredictResource(Resource):
    def post(self):
        """
        Handle POST requests for /predict
        Returns a JSON object with prediction result
        """
        try:
            # Get JSON data from the request
            data = request.get_json()
            if data is None:
                return jsonify({'error': 'No data provided'}), 400
            # Perform prediction using NumPy (example)
            prediction = np.mean(data['features'])
            return jsonify({'prediction': prediction})
        except Exception as e:
            # Return error message in case of an exception
            return jsonify({'error': str(e)}), 500

# Add the resource to the API
api.add_resource(PredictResource, '/predict')

if __name__ == '__main__':
    # Run the Flask app
    app.run(debug=True)